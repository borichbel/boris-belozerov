-- Movies Table
CREATE TABLE public.streaming_service_movies (
    film_id INTEGER PRIMARY KEY,
    title TEXT,
    year INTEGER,
    genre TEXT,
    rating DECIMAL(3,1),
    duration_min INTEGER,
    country TEXT,
    director TEXT
);

-- Awards Table
CREATE TABLE public.streaming_service_awards (
    award_id INTEGER PRIMARY KEY,
    film_id INTEGER REFERENCES public.streaming_service_movies(film_id) ON DELETE CASCADE,
    name_award TEXT,
    nomination TEXT,
    year INTEGER
);

-- Soundtrack Table
CREATE TABLE public.streaming_service_soundtrack (
    track_id INTEGER PRIMARY KEY,
    film_id INTEGER REFERENCES public.streaming_service_movies(film_id) ON DELETE CASCADE,
    song_name TEXT,
    performer TEXT,
    duration INTEGER
);

-- Collection Table
CREATE TABLE public.streaming_service_collection (
    collection_id INTEGER PRIMARY KEY,
    name_coll TEXT,
    description TEXT
);

-- Film in Collection Table 
CREATE TABLE public.streaming_service_film_collection (
    film_collection_id INTEGER PRIMARY KEY,
    film_id INTEGER REFERENCES public.streaming_service_movies(film_id) ON DELETE CASCADE,
    collection_id INTEGER REFERENCES public.streaming_service_collection(collection_id) ON DELETE CASCADE
);

-- Actor Table
CREATE TABLE public.streaming_service_actor (
    actor_id INTEGER PRIMARY KEY,
    full_name TEXT,
    age INTEGER,
    gender TEXT,
    country TEXT
);

-- Actor in Film Table 
CREATE TABLE public.streaming_service_actor_film (
    actor_film_id INTEGER PRIMARY KEY,
    film_id INTEGER REFERENCES public.streaming_service_movies(film_id) ON DELETE CASCADE,
    actor_id INTEGER REFERENCES public.streaming_service_actor(actor_id) ON DELETE CASCADE,
    role_type TEXT,
    character TEXT
);

